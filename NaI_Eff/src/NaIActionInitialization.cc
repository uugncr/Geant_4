/// \file NaIActionInitialization.cc
/// \brief Implementation of the NaIActionInitialization class

#include "NaIActionInitialization.hh"
#include "NaIPrimaryGeneratorAction.hh"
#include "NaIRunAction.hh"
#include "NaIEventAction.hh"
#include "NaISteppingAction.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

NaIActionInitialization::NaIActionInitialization()
 : G4VUserActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

NaIActionInitialization::~NaIActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void NaIActionInitialization::BuildForMaster() const
{
  NaIRunAction* runAction = new NaIRunAction;
  SetUserAction(runAction);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void NaIActionInitialization::Build() const
{
  SetUserAction(new NaIPrimaryGeneratorAction);

  NaIRunAction* runAction = new NaIRunAction;
  SetUserAction(runAction);
  
  NaIEventAction* eventAction = new NaIEventAction(runAction);
  SetUserAction(eventAction);
  
  SetUserAction(new NaISteppingAction(eventAction));
}  

